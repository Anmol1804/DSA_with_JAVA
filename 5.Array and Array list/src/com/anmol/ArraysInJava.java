package com.anmol;

public class ArraysInJava {

    public static void main(String[] args) {
	    /*
	    Syntax
	    datatype[] array_name = new datatype[size];
	    */

        int[] rnos = new int[5];
        int[] nos = {1,2,3,4,5};
        System.out.println(rnos[0]);
        System.out.println(nos[1]);

        String[] arr = new String[4];
        System.out.println(arr[0]);

    }
}
